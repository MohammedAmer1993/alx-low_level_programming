#include "function_pointers.h"
/**
  * op_add - addition func
  * @a: frist num
  * @b: second num
  * Return: the sum
  */

int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - subtracion func
  * @a: frist num
  * @b: second num
  * Return: the subtraction
  */

int op_sub(int a, int b)
{
	return (a - b);
}

